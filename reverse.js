var num = 1;
while (num <= 10) {
    console.log(num);
    num ++;
}

//while loop reverse way
console.log('reverse way in while loop:');
var num = 10;
while (num >= 1) {
    console.log(num);
    num --;
}


//for loop reverse way
console.log('reverse way in for loop:');
for (var i = 10; i >= 1; i--){
    console.log(i);
}